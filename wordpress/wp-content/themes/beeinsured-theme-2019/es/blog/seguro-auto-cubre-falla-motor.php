      <div class="title-section"><h1>¿Seguro de auto cubre falla de motor?</h1></div>
        Mantener un <strong class="bold">auto en excelentes condiciones</strong> y cuidar su desgaste es el propósito del seguro de auto. Sin embargo, hay límites a lo que cubre el seguro de auto comercial. El fallo del motor es uno de estos límites. Las circunstancias del daño a su motor determinarán si su seguro pagará o no para repararlo. En la mayoría de los casos, su <strong class="bold">seguro cubrirá los costos de daños menores a su motor</strong> si es como resultado de un accidente. Sin embargo, si el daño es demasiado grave o si causó el accidente, es posible que no esté cubierto.
        <div class="boxheader">Las circunstancias del daño a su motor determinarán si su seguro pagará o no para repararlo.</div>
        Hay dos tipos de seguro de auto. El primero es el <strong class="bold">seguro de responsabilidad civil</strong> básico y solo cubre daños al vehículo de otro conductor después de un accidente que usted cause. No cubre los daños ocasionados por su propio vehículo y no cubrirá el costo de reparar o reemplazar un motor dañado. La cobertura integral es el segundo <strong class="bold">seguro de automóvil</strong> y pagará la reparación de los costos de su motor solo si el daño es el resultado de vandalismo, inundaciones u otras fuerzas externas. <br><br> La cobertura de colisión cubre daños a su propio vehículo después de un accidente. El fallo del motor suele ser un problema de fabricación (puede estar cubierto por la garantía) o desgaste (si el vehículo es realmente viejo).

