        <strong class="bold">El seguro de equipos y herramientas para contratistas</strong> está diseñado para proteger todas las <strong class="bold">herramientas y equipos de su comercio</strong> contra robos, vandalismo y daños físicos (sin incluir el desgaste general). Esta cobertura suele ser obligatoria al alquilar o arrendar equipos 
