<div class="title-section"><h1>¿Por qué comprar una póliza de general liability?</h1></div>
El seguro de responsabilidad general protege a una empresa contra daños a la propiedad, reclamaciones por lesiones publicitarias, reclamaciones por lesiones personales y reclamaciones por lesiones corporales. Estas afirmaciones podrían poner en peligro el éxito y la reputación de cualquier negocio. El seguro de <strong class="bold">responsabilidad general (GL)</strong>, también conocido como seguro de responsabilidad comercial, es una cobertura que puede protegerlo de una variedad de reclamos, incluyendo lesiones corporales, daños a la propiedad, lesiones personales y otros que pueden surgir de sus operaciones comerciales.<br><br>
<h2 class="sub">5 cosas que generalmente cubre el seguro de general liability</h2>
<div class="dot">Responsabilidad civil por lesiones corporales y daños materiales.</div>
<div class="dot">Lesiones personales y publicitarias. </div>
<div class="dot">Pagos médicos </div>
<div class="dot">Productos terminados operaciones.</div>
<div class="dot">Daños a locales alquilados a usted.</div>
<br>      
Sin embargo, puede elegir los tipos de riesgos que desea cubrir al elegir el tipo correcto de póliza con las características que necesita. Protege al comprador (el "asegurado") de los riesgos de responsabilidades impuestas por demandas y reclamos similares. Protege al asegurado en caso de que sea demandado por reclamos que se encuentren dentro de la cobertura de la póliza de seguro. La responsabilidad general es requerida por la ley en la mayoría de los estados.
<div class="boxheader">La cobertura de GL está diseñada para proteger a los propietarios de negocios de California de daños directos o indirectos a otra parte.</div>
        En California, por ejemplo, el estado exige un seguro de responsabilidad civil general para la mayoría de los dueños de negocios. La cobertura de GL está diseñada para <strong class="bold">proteger a los propietarios de negocios de California</strong> de daños directos o indirectos a otra parte. Los dueños de negocios en California tienen una opción con respecto a los deducibles de seguro y los límites de la póliza dentro de su cobertura.
