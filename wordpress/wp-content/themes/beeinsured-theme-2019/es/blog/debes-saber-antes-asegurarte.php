      <div class="title-section"><h1>Esto es lo que debes saber antes de asegurarte</h1></div>
  El seguro es un producto financiero vendido por las compañías de <strong class="bold">seguros para protegerlo a usted y (o) a su propiedad contra el riesgo de pérdida, daño o robo.</strong> Si realiza una reclamación, su aseguradora pagará la pérdida que está cubierta por la póliza. Si no lo hace, no recuperará su dinero, sino que se combinará con las primas de otros asegurados que han contratado un seguro con la misma compañía de seguros. Si realiza una reclamación, el dinero proviene de las primas de los titulares de pólizas.<br><br><h2 class="sub">5 razones por las que los servicios de seguros son tan importantes</h2>
<div class="dot">Ayuda a protegerlo a usted y a su familia contra la crisis financiera.</div>
        <div class="dot">Reduce el estrés en tiempos difíciles como lesiones, muerte, etc. </div>
        <div class="dot">El pago del seguro ayuda a brindar seguridad financiera a usted y su familia.</div> 
        <div class="dot">Da tranquilidad.</div>
        <div class="dot">Da un legado para ayudar a asegurar el nivel de vida de su familia si usted muere.</div><br><h3 class="sub">¿Puede una aseguradora negarse a pagar mi reclamo?</h3>
 <strong class="bold">Una aseguradora solo puede negarse a pagar una reclamación solo si:</strong><br>
        <div class="dot">La Póliza no estaba vigente cuando sucedió lo que reclama. </div>
        <div class="dot">La Póliza no es válida porque no dijo la verdad cuando solicitó un seguro o no reveló algo que pudiera afectar su reclamo. </div>
        <div class="dot">La Póliza no es válida porque retuvo deliberadamente o por descuido información o engañó a sus aseguradores. </div>
        <div class="dot">El artículo no está cubierto por su póliza.</div>
        <div class="dot">Hay una cláusula de exclusión en la Póliza, lo que significa que no puede reclamar por lo que sucedió, se perdió algunas de las cuotas de su prima</div>
        <br><div class="boxheader">El costo del seguro del contratista está determinado por la cobertura, la compañía de seguros y el estado en que se encuentra. </div>
