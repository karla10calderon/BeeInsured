      <div class="title-section"><h1>What does excess and umbrella insurance covers?</h1></div>
       <h2 class="sub"> Umbrella and Excess Liability policies are designed to provide coverage above the limits of underlying coverage. </h2><br>
        Umbrella insurance safeguards your assets and it also protects you from lawsuits. It is not only limited to this.<strong class="bold"> Umbrella insurance can most certainly cover professional liability.</strong> A business umbrella policy only provides liability supplements to existing business liability policies. Also, some professionals are prone to malpractice lawsuit like the healthcare and legal fields.
        <div class="boxheader">A business umbrella policy only provides liability supplements to existing business liability policies.</div>
        If you are sued and the limits are exceeded on your malpractice policy, your business umbrella policy can help to pay up to the policy coverage limit.<strong class="bold"> Umbrella insurance also covers lawsuits and liability claims </strong>that do not result in legal action this is because protecting assets against potential lawsuits is the main motivator for many people to purchase umbrella insurance.<br><br>
        One of the coverages of umbrella insurance is property damage in most but not all circumstances. It covers dog bites under most circumstances. For example, if your dog bites your neighbor, you are liable for that person’s bodily injury. <strong class="bold">Umbrella insurance will also cover</strong> you if your neighbor decides to seek legal recourse. However, umbrella insurance normally does not cover flood damage and the majority of umbrella insurance policies do not cover uninsured motorists.<br><br>
        An <strong class="bold">umbrella policy is meant to cover any property damage or bodily injury you cause.</strong> Umbrella insurance is a good policy to have if you want to make sure you have coverage in unpredictable situations, including when you rent a car. If you are a renter and have a renter’s insurance policy, you can purchase an umbrella policy to extend your liability limits beyond the standard coverage. An umbrella policy can be a good investment for landlords. 


