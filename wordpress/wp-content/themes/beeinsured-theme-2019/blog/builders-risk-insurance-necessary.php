</h1></div>

        <h2 class="sub">Builders risk insurance is the insurance against the risk of loss from damage to property under construction, whether new construction or renovation of existing buildings.</h2><br> Builders risk coverage presents issues not normally associated with property coverage for existing buildings in use. A sublimit is an amount that is less than the total limits for the project and the maximum amount that the insurance carrier will pay in the event of a covered loss or damage resulting from a specific peril, construction type or type of property however, the contractor is responsible for the per occurrence deductible and this is stipulated in the contract. Insurance coverage begins on the notice to proceed date for the construction phase.  Builder’s risk insurance must be requested prior to work starting on the project site.
        <br><br>
        <div class="boxheader"> Builder’s risk insurance must be requested prior to work starting on the project site.</div>
        Coverage is continued and not terminated until the date of final completion recited in a notice of completion filed for the construction phase. If a project is going to continue beyond that date, the Project Manager or designee must complete and submit a Builder’s Risk Extension form,however, it can be terminated earlier when 100% of the work is completed and all the paperwork completed. In addition, if the project is over the  <strong class="bold">policy limit or the sublimit for wood frame construction</strong> then additional time is needed to secure coverage from another carrier. For projects with more than one construction type, the rate for the predominant construction type is applicable if the value of that construction type is 80% or greater. <br><br>
        The construction cost reported for  <strong class="bold">Builder’s Risk insurance should be the Contract Sum for the construction phase,</strong> including but not limited to all labor, fees, general conditions, architect, engineering, and professional fees also, costs for materials, equipment, machinery, and supplies that will be used in the construction and will become part of the completed project.<br><br>
        The equipment purchased by the university from other vendors which will be installed prior to final project completion, the  <strong class="bold"> equipment values should be included in the construction cost.</strong>  Any significant increase in construction cost (5% increase or more) and/or significant change in scope of work must be reported to the insurance company. If a project goes over the policy limit during the course of construction the insurance company must be notified immediately so the carrier can be notified and approval obtained for an increase in limits.  

