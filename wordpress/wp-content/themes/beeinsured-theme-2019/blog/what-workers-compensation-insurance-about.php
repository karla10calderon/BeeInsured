      <div class="title-section"><h1>What is workers’ compensation insurance about?</h1></div>
No matter how small your business is, as long as you have people working for you especially if there are occupational hazards or illness associated with your industry, <strong class="bold"> you need to get workers’ compensation!</strong>
<br><br>
        Workers' compensation is a compulsory type of business insurance that provides employees who become injured or ill while on the job with medical coverage and income replacement. It also <strong class="bold">protects companies from being sued by employees for the workplace</strong> conditions that caused such an injury or illness. Businesses are required by law in all fifty states to pay for the medical treatment and lost wages of employees who suffer job-related injuries or illnesses. In order to avoid exorbitant expenses of paying for workers’ accidents and illness on the job, companies buy workers' compensation insurance policies.<br><br>
<div class="boxheader"> Most compensation plans offer coverage of medical fees related to injuries acquired as a direct result of employment.</div>
        Most states give businesses the choice of buying workers' compensation policies either directly from the state or from a private insurer. Each state determines its own system's payment schedules, employee’s eligibility requirements, and rehabilitation procedures. Most compensation plans offer coverage of medical fees related to injuries acquired as a direct result of employment. For example, <strong class="bold"> a construction worker could claim compensation if scaffolding fell on their head,</strong> but not if they were in a traffic accident while driving to the job site. In other situations, workers can receive the equivalent of sick pay while they are on medical leave. If a worker dies as a result of their employment, workers' compensation also gives payments to their family members or other dependents. 
 <br><br>
