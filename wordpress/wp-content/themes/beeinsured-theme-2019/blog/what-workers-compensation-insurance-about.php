        <div class="dot">Your employer must carry workers' compensation insurance.</div>
        <div class="dot">You must have a work-related sickness.</div>
        <div class="dot">You must meet your state's deadlines for reporting the injury and filing a workers' compensation claim.</div>
        <div class="dot">Employees’ medical expenses for illness and injury suffered due to on the job accidents.</div>
        <div class="dot">Repetitive stress injuries.</div>
        <div class="dot">Workplace violence.</div>
        <div class="dot">Stress-related mental injuries, like PTSD.</div>
        <div class="dot">Death benefits for employees’ dependents.</div>
        <div class="dot">Rehabilitation services and retraining programs.</div>

