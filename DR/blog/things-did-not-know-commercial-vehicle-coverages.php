      <div class="title-section"><h1>Things you probably did not know about commercial vehicle coverages</h1></div>

        Commercial vehicle insurance is a <strong class="bold">policy of physical damage and liability coverages for amounts, situations, and usage </strong>not covered by a personal auto policy. This type of insurance covers a variety of vehicles used for business purposes ranging from automobiles used for business, including company cars, to a wide variety of commercial trucks. Box trucks, food trucks, work vans, and service utility trucks are also a few examples of <strong class="bold">larger commercial vehicles which also require coverage. </strong>Commercial vehicle insurance also covers the employees operating the vehicle and the equipment inside. 
        <div class="boxheader">Pays for bodily injury or death resulting from an accident for which you are at fault.</div>
        <h2 class="sub">7 things that Commercial vehicle insurance can cover.</h2><br>
        <div class="dot"><b>Bodily injury liability coverage:</b><br>Pays for bodily injury or death resulting from an accident for which you are at fault.</div>

        <div class="dot"><b>Property damage liability coverage:</b><br>Provides you with protection if your vehicle accidentally damages another person's property.</div>
       
        <div class="dot"><b>Combined single limit:</b><br>Liability policies typically offer separate limits that apply to bodily injury claims for property damage. A combined single limits policy has the same dollar amount of coverage per covered occurrence whether bodily injury or property damage, one person or several.</div>
        
        <div class="dot"><b>Medical payments, no-fault or personal injury coverage:</b><br>Usually pays for the medical expenses of the driver and passengers in your vehicle incurred as a result of a covered accident regardless of fault.</div>
        
        <div class="dot"><b>Uninsured motorist coverage:</b><br>Pays for your injuries and, in some circumstances, certain property damage caused by an uninsured or a hit-and-run driver. In some cases, underinsured motorist coverage is also included. This is for cases in which the at-fault driver has insufficient insurance.</div>
        
        <div class="dot"><b>Comprehensive physical damage coverage:</b><br>Pays for damage to your vehicle from theft, vandalism, flood, fire, and other covered perils.</div>
        
        <div class="dot"><b>Collision coverage:</b><br>Pays for damage to your vehicle when it hits or is hit by another object.</div>
        <br>
        Most states require that all registered vehicles be covered by liability insurance. Some states even require you to purchase uninsured motorist coverage and no-fault car insurance.
         
