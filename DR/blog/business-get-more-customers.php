      <div class="title-section"><h1>Ways my business can get more customers!</h1></div>

        A <strong class="bold">business loan is a debt that the company is required to pay back</strong> according to the loan’s terms and conditions. A business loan is borrowed capital that companies use toward expenses when they are unable to pay for themselves. You can use business loans to pay for salaries and wages until their new company gets off the ground, while you can also put borrowed funds toward office supplies, inventory or business projects.<br>
When a loan is borrowed, lenders want to know how the business intends to use the borrowed money, so business owners must make sure to have a clear outline of how the money will be spent. 
        <div class="boxheader">The loan amount will be considered a liability should you apply for additional loans.</div>      
The amount of money you borrow should be enough to cover the expenses. But remember, the loan amount will be considered a liability should you apply for additional loans. A business loan is good if you want to avoid investing all your own savings and are unable to find other investors because it is a simple way of raising the necessary capital to start your business venture. 
        For any <strong class="bold">business loan borrowed, </strong>there is always the payment of interest and you need to choose either a fixed or variable interest rate. Also, the interest that is being paid on the loan is tax deductible but the amount that represents principal is not. When putting together your business plan, you need to set a budget for all predicted expenses. Your personal credit matters a lot when borrowing a business loan. Most lenders do, in fact, look at your personal credit history when deciding whether to grant you a business loan. Your<strong class="bold"> credit score is seen as a direct reflection</strong> of how capable you are with money and how dependable you are as a borrower. If you can’t handle your personal finances well, a lender will assume you won’t be able to handle your business finances well, either.


