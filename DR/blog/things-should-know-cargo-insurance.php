      <div class="title-section"><h1>Things you should know about cargo insurance</h1></div>
        <h2 class="sub"> Cargo insurance provides coverage against all risks of physical loss or damage to freight during the shipment from any external cause during shipping, whether by land, sea or air.</h2><br> When cargo is a primary conveyance it means that it is the goods and merchandise that is being transported and the main part of the transportation is by ship. Cargoes can be insured as long as the countries involved can legally trade with the United States. When your freight forwarder tells you that they have insurance, it simply means they have insurance however, you and your cargo do not.
        <div class="boxheader"> Cargoes can be insured as long as the countries involved can legally trade with the United States. </div>
        If you ship cargo regularly, then you can have an annual open  <strong class="bold">cargo policy automatically insures your shipments</strong> on set terms, conditions and rate without the need to contact your insurance broker or company each time. If you don’t want to go through the stress of an insurance company, freight forwarders have a cargo insurance policy in their name where they can insure their client’s cargo. <br><br>
        However, the terms and conditions are usually very limited, general in nature and may not suit all to your needs – the pricing is usually quite high too. It is better to deal with the insurance broker and not the company because the insurance broker represents you in all negotiations and transactions with the insurers.   <strong class="bold">By dealing directly with an insurance company</strong> you only receive their point of view and the products they offer, along with the price they stipulate.<br> <br>
        When you sell your goods on cost and freight (C & F) terms, it is better to buy your own cargo open insurance policy because you sell on C. & F. terms you retain title to the goods (and therefore ownership) until the point in the transit chain where title transfers over to your buyer. Even if you buy goods on cost, insurance and freight terms there is still needing to buy cargo insurance open policy so that you have control, and knowledge, of the insurance terms, price and insurance company selected. If you have never had a cargo loss before you still need cargo insurance because of the risks that you ran every time that your goods are in transit.<br><br>
        The fact that your goods have never been lost or damaged does not mean that it won’t in the future. To  <strong class="bold">get a cargo insurance quote is very easy</strong>, just fill out the application with your details and submit to an insurance company of your choice. You will get a response within 24 – 48 hours. Perils only coverage is not enough coverage for your good, you need all risks coverage so that you will be protected. It is not required to have title to the goods when you take out the cargo insurance, however, you’ll need to demonstrate title should a loss occur, and you make a claim on the policy.<br><br>
        If your  <strong class="bold">cargo is going by various transportation mode like truck</strong> to port ocean vessel overseas then rail and also truck to its final destination then you can use the main mode of transport, that is the ocean vessel – the other modes of transport are regarded as connecting conveyances and are automatically included as being in the normal course of transit, provided that your cargo insurance has been structured correctly. 
