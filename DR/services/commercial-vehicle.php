           <div class="dot"><b>Bodily injury liability coverage:</b><br>Pays for bodily injury or death resulting from an accident for which you are at fault and in most cases provides you with a legal defense.</div><br>
   
       
        
        <div class="dot"><b>Property damage liability coverage</b> – provides you with protection if your vehicle accidently damages another person's property and in most cases provides you with a legal defense.<br><br></div>
        
        <div class="dot"><b>Combined single limit (CSL)</b> – Liability policies typically offer separate limits that apply to bodily injury claims for property damage. A combined single limits policy has the same dollar amount of coverage per covered occurrence whether bodily injury or property damage, one person or several.<br><br></div>
        
        <div class="dot"><b>Medical payments, no-fault or personal injury coverage</b> – usually pays for the medical expenses of the driver and passengers in your vehicle incurred as a result of a covered accident regardless of fault.<br><br></div>
        
        <div class="dot"><b>Uninsured motorist coverage</b> – pays for your injuries and, in some circumstances, certain property damage caused by an uninsured or a hit-and-run driver. In some cases, underinsured motorist coverage is also included. This is for cases in which the at-fault driver has insufficient insurance.<br><br></div>
        
