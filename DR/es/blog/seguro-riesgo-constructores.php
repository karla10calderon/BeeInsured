        <h2 class="sub">     El seguro de riesgo de los constructores es el seguro contra el riesgo de pérdida por daños a la propiedad en construcción, ya sea nueva construcción o renovación de edificios existentes.</h2><br>La cobertura de riesgo de los constructores presenta problemas que normalmente no están asociados con la cobertura de propiedad para los edificios existentes en uso. Un sub limit es una cantidad que es menor que los límites totales para el proyecto y la cantidad máxima que la compañía de seguros pagará en caso de una pérdida o daño cubierto resultante de un peligro específico, tipo de construcción o tipo de propiedad, sin embargo, el contratista es responsable del deducible por ocurrencia y esto se estipula en el contrato. La cobertura del seguro comienza en la notificación para proceder a la fecha de la fase de construcción. El seguro de riesgo del constructor debe solicitarse antes de comenzar el trabajo en el sitio del proyecto. 
        <br><br>
        <div class="boxheader">La cobertura continúa y no finaliza hasta que se recita la fecha de finalización final en un aviso de finalización archivado para la fase de construcción. </div>
        
        
       La cobertura continúa y no finaliza hasta que se recita la fecha de finalización final en un aviso de finalización archivado para la fase de construcción. Si un proyecto va a continuar más allá de esa fecha, el Gerente del Proyecto o su designado deben completar y enviar un formulario de Extensión de Riesgo del Constructor, sin embargo, se puede terminar antes cuando se complete el 100% del trabajo y se complete todo el papeleo. Además, si el proyecto <strong class="bold">supera el límite de la póliza o el sub límite para la construcción</strong>  de estructuras de madera, entonces se necesita tiempo adicional para asegurar la cobertura de otro transportista. Para proyectos con más de un tipo de construcción, la tasa para el tipo de construcción predominante es aplicable si el valor de ese tipo de construcción es 80% o más.<br><br>
        
       El costo de construcción reportado para <strong class="bold">el seguro de Riesgo del Constructor debe ser la Suma del Contrato para la fase de construcción,</strong>  que incluye, entre otros, mano de obra, tarifas, condiciones generales, arquitectura, ingeniería y honorarios profesionales, costos de materiales, equipos, maquinaria y suministros que Se utilizará en la construcción y formará parte del proyecto finalizado.<br><br>
        El equipo comprado por la universidad a otros proveedores que se instalará antes de la finalización del proyecto final, los <strong class="bold"> equipment values should be included in the construction cost.</strong> Cualquier aumento significativo en el costo de construcción (aumento del 5% o más) y / o cambio significativo en el trabajo debe informarse a la compañía de seguros. Si un proyecto supera el límite de la póliza durante el curso de la construcción, se debe notificar inmediatamente a la compañía de seguros para que se notifique al transportista y se obtenga la aprobación para un aumento en los límites.  

