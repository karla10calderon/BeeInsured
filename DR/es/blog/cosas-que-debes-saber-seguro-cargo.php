      <div class="title-section"><h1>Cosas que debes saber sobre el seguro de carga</h1></div>
            
        <h2 class="sub">  El seguro de carga proporciona cobertura contra todos los riesgos de pérdida física o daños a la carga durante el envío por cualquier causa externa durante el envío, ya sea por tierra, mar o aire.</h2><br> Cuando la carga es un medio de transporte principal, significa que son los bienes y mercancías que se transportan y la parte principal del transporte es por barco. Las cargas se pueden asegurar siempre que los países involucrados puedan comerciar legalmente con los Estados Unidos. Cuando su carga reenviada le dice que tienen seguro, simplemente significa que tienen seguro, sin embargo, usted y su carga no. 
        <div class="boxheader">Las cargas se pueden asegurar siempre que los países involucrados puedan comerciar legalmente con los Estados Unidos.</div>
        Si envía la carga con regularidad, entonces puede tener una <strong class="bold">póliza anual de carga abierta que asegure automáticamente</strong> sus envíos en términos, condiciones y tarifas establecidos sin la necesidad de contactar a su agente de seguros o compañía cada vez. Si no desea pasar por el estrés de una compañía de seguros, los transitorios tienen una póliza de seguro de carga a su nombre donde pueden asegurar la carga de sus clientes.<br><br>

        Sin embargo, los términos y condiciones son generalmente muy limitados, de naturaleza general y pueden no satisfacer todas sus necesidades; el precio también suele ser bastante alto. Es mejor tratar con el corredor de seguros y no con la compañía porque el corredor de seguros lo representa en todas las negociaciones y transacciones con los aseguradores.  <strong class="bold">Al tratar directamente con una compañía de seguros,</strong> solo recibe su punto de vista y los productos que ofrecen, junto con el precio que estipulan.
 <div class="boxheader"> Si compra bienes en términos de costo, seguro y flete, todavía tiene que comprar una póliza abierta de seguro de carga.</div>   
Cuando vende sus productos en términos de costo y flete (C & F), es mejor comprar su propia póliza de seguro de carga abierta porque vende en términos C. y F. usted retiene el título de los bienes (y, por lo tanto, la propiedad) hasta el punto en la cadena de tránsito donde el título se transfiere a su comprador. Incluso si compra bienes en términos de costo, seguro y flete, todavía tiene que comprar una póliza abierta de seguro de carga para que tenga control y conocimiento de los términos, precio y compañía de seguros seleccionados. Si nunca ha tenido una pérdida de carga, todavía necesita un seguro de carga debido a los riesgos que corrió cada vez que sus mercancías están en tránsito.<br><br>

El hecho de que sus bienes nunca se hayan perdido o dañado no significa que no se vayan a usar en el futuro. Para obtener una cotización de seguro de carga es muy fácil, solo complete la solicitud con sus datos y envíela a la compañía de seguros de su elección. Recibirá una respuesta dentro de 24 a 48 horas. La cobertura de solo riesgos no es suficiente para su bien, usted necesita cobertura de todos los riesgos para estar protegido. No es necesario tener el título de los bienes cuando contrata el seguro de carga, sin embargo, deberá demostrar el título en caso de que se produzca una pérdida, y debe presentar un reclamo sobre la política.<br><br>

        Si su  <strong class="bold">carga se desplaza en varios modos de transporte</strong>, como un camión a otro puerto oceánico en el extranjero, luego el ferrocarril y el camión a su destino final, puede usar el modo principal de transporte, es decir, el barco oceánico: los otros modos de transporte se consideran como conexión. Los medios de transporte se incluyen automáticamente en el curso normal del tránsito, siempre que su seguro de carga se haya estructurado correctamente.
