    <p>Every business needs to <strong>protect themselves from accidents or injuries</strong> that may occur on their business grounds.<br><br>
        <strong>General Liability insurance coverage</strong> can protect your business from these unexpected expenses, so it is essential that you understand what it does and doesn't cover.<br><br>
        
    General liability covers business related incidents that result in bodily injury to a third-party. This can include a customer slipping and falling on your premises, or an employee accidentally dropping a toolbox on someone’s foot. Of course, this coverage only applies to non-employee injuries.<br><br>
        
    Additionally, general liability protects any third-party property damage resulting from your business operations. For example, a landscaper’s policy may cover a stone that kicks up from their lawn mower and breaks a client’s window.<br><br>
<b>General liability insurance can cover</b> <br><br>

    -Expenses to repair at a business. <br>
-Expenses to replace customer property that is damaged at a business. <br>
-Professional mistakes. <br>
-Intentional damage. <br>
-Bodily injury and property damage liability<br>
-Personal and advertising injury<br>
-Legal Fees<br>
-Medical payments<br>
-Products completed operations<br>
-Damage to premises rented to you<br>
 

