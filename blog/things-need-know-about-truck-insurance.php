      <div class="title-section"><h1>The things that you need to know about truck insurance</h1></div>
        
        <strong class="bold">Commercial truck insurance provides liability insurance for truckers</strong> who carry the cargo of others and is used in the event that the cargo is destroyed, damaged, stolen, or otherwise declared a loss while in the process of being shipped. The difference between <strong class="bold"> primary liability and secondary liability</strong> (i.e. bobtail, unladen, non-trucking) is that primary liability coverage is protection for the public and is required by the Federal Motor Carrier Safety Administration (FMCSA) to obtain your own authority while secondary liability coverage is required by your motor carrier and only covers you while under a permanent signed lease with them.
        <div class="boxheader">It is required that the motor carrier whose authority you are running under to obtain the Primary Liability and Cargo coverage.</div>
The minimum coverage that you need to get your authority is $750,000 primary liability coverage required by FMCSA, but most shippers/brokers require $1,000,000 to load you. For truck insurance, the coverage you need to get tags is primary liability coverage. The purpose of physical damage if your truck is parked or there is no lienholder it that it covers the investment you have made in your truck.<br><br>
        It is required that the motor carrier whose authority you are running under to obtain the  <strong class="bold">Primary Liability and Cargo coverage.</strong> The difference between actual cash value and stated limit is that there is no value set on equipment as actual cash value is the cost of your equipment at the time of a loss minus depreciation while a stated limit policy is when the insured places a value on their equipment. <br><br>
        There no age restrictions on the passenger accident coverage. To run under your own authority, <strong class="bold"> Auto Liability and Cargo insurance are required.</strong><br><br> You are required to have higher limits of auto liability and cargo insurance if you are a car hauler with your own authority. The coverages you need if you lease on to a motor carrier should be specified in your lease agreement with the motor carrier. <strong class="bold">The difference between surplus lines and admitted carriers</strong> is that admitted carriers are licensed in a particular state to do business and is regulated by each state’s Division of Insurance while a surplus line’s carrier is not required to be licensed in your state but do need to be licensed in the state in which they are domiciled. 
