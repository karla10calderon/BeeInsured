        Insurance is a financial product sold by insurance companies to <strong class="bold">protect you and (or) your property against the risk of loss, damage or theft.</strong> If you make a claim your insurer will pay out for the loss that is covered under the policy. If you don’t, you won’t get your money back but instead, it is pooled with the premiums of other policyholders who have taken out insurance with the same insurance company. If you make a claim the money comes from the pool of policyholders’ premiums.<br><br>
        <h2 class="sub">Reasons why Insurance services are so important</h2>
        
        <div class="dot">It helps to protect you and your family against financial crisis.</div>
<div class="dot">It reduces stress during difficult times like injury, death etc.</div>
<div class="dot">Insurance payout helps gives financial security to you and your family.</div>
<div class="dot">It gives peace of mind.</div>
<div class="dot">It gives a legacy to help secure the standard of living of your family if you die.</div>
<br>
        <h3 class="sub">Can an insurer refuse to pay my claim?</h3>
<strong class="bold">An insurer can only refuse to pay a claim only if:</strong><br>
        <div class="dot">The policy was not in force when what you're claiming for happened.</div>
        <div class="dot">The policy is invalid because you didn't tell the truth when you applied for insurance or failed to disclose something which could affect your claim.</div>
        <div class="dot">The policy is invalid because you deliberately or carelessly withheld information or misled your insurers.</div>
        <div class="dot">The item is not covered by your policy.</div>
        <div class="dot">There is an exclusion clause in the policy which means that you can't claim for what's happened you've missed some of the installments of your premium.</div>
