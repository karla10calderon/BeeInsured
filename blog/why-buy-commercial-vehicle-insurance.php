      <div class="title-section"><h1>Why buy a commercial vehicle insurance?</h1></div>

        <strong class="bold">Commercial vehicle insurance</strong> is very important in taking care of our vehicles but it there are limits on the coverage. Basic auto insurance covers a wide range of protection, including collision coverage that pays for your damaged car to be repaired as a result of a car accident. But a <strong class="bold">basic auto insurance policy doesn't typically cover the cost of mechanical repairs</strong> unless they are an immediate result of a collision. In that case, once you've met your deductible, your collision coverage would reimburse you for the expense of the mechanical repair.
Some car owners are under the impression that comprehensive coverage will help pay for mechanical repairs, but it won’t. After you meet your deductible, comprehensive coverage may reimburse you for damage caused to your car by something other than an accident, such as a falling object, hail, or vandalism. 
        
        <div class="boxheader">Coverage from your primary auto insurance will extend to a rental vehicle</div>
More so, business vehicle insurance doesn’t usually cover engine damage but it usually covers engine damage under the following circumstances: a collision that breaks something inside the engine, water damage that causes the engine to stop working, the engine catching fire and vandalism of the engine. It is important to note that engine damage will only be covered under the applicable coverage that you have taken out.<br><br>
However, if you mistreat your vehicle and cause damage to the engine that could have otherwise been prevented, you will not be covered from your insurance policy. These damages include: the engine breaking from driving with no oil, the time-belt snapping causing damages to the engine, mechanical parts of the engine failing and wear and tear on the engine.
        Also, coverage from your primary auto insurance will extend to a rental vehicle. If you cause an accident while driving the rental, your liability insurance would pay up to your policy limits for the damages to other cars or property. Likewise,  <strong class="bold">collision coverage on your regular policy would pay for accident-related damages to the rental car</strong> you're driving. Finally, your comprehensive coverage would take care of damages to the rental vehicle not related to a traffic accident, such as theft or vandalism.
