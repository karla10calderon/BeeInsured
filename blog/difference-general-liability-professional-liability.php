      <div class="title-section"><h1>What is the difference between general and professional liability?</h1></div>
      
        If you want your  <strong class="bold">business to be a success,</strong> itis important to protect your business against lawsuits. Protecting against lawsuits means that you have to be insured and also understand which types of small business insurance you need. Most liability claims costs can be covered with general or professional liability insurance. A better understanding of the differences between general and <strong class="bold"> professional liability insurance</strong> is very important in an environment in which the threat of lawsuits continues to grow.
General and professional liabilities are both ways of protecting your business and because they perform the same function, both terms general and professional liability insurance are often confused. 
        <div class="boxheader">General liability helps cover the costs of damages and lawsuits if your business is held responsible for things like property damage, bodily injury, libel, and slander against another.</div>
        
        General liability helps cover the costs of damages and lawsuits if your business is held responsible for things like property damage, bodily injury, libel, and slander against another. Well, if someone trips and falls at your office, it’s <strong class="bold"> general liability insurance that helps protect your business.</strong> But what if custom programming services provided to a client caused that client to suffer some damages financially? General liability insurance doesn’t cover that. It is the same with if you lose or corrupt a customer’s data? General liability insurance won’t cover that either. Professional liability helps cover the costs related to claims your business committed errors or omissions in the advice or services it provided.<br><br>
        <h2 class="sub">Specifically, general liability insurance covers the costs of claims:</h2><br>
        <div class="dot"><b>Made against your business because someone injured herself on your premises.</b><br> For example, a customer gets injured on a construction site. </div>
        <div class="dot"><b>Made against your business because you damaged someone else’s property.</b><br> For example, if you’re working on a job site and damage the neighbour’s lawn, the neighbour could make a claim against your business.</div>
        <div class="dot"><b>Made against your business because your advertising infringed on someone else’s copyrighted material.</b></div>
        <div class="dot"><b>Professional insurance covers the cost of claims made against your business because of errors in your provision of advice or services..</b><br> For example, a caterer mixes up the order on a couples’ wedding ceremony. The married couple makes a claim against the caterer for errors in his service. The caterer’s professional liability insurance can help cover the cost of this claim.</div>

