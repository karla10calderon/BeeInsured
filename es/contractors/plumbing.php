      <div class="title-section"><h1>Plomería</h1></div>
        
        Un contratista de plomería <strong class="bold">proporciona un medio para el suministro de agua segura, de gran volumen y de temperatura adecuada para el propósito previsto y la eliminación adecuada de los desechos líquidos</strong> de las instalaciones en todas las estructuras y trabajos fijos. Esta clasificación incluye pero no se limita a: <br><br>
        <div class="dot"><b>(a)</b> Eliminación completa de desechos de los locales o la construcción y conexión de sistemas de eliminación de desechos en el sitio.</div><br>
        <div class="dot"><b>(b)</b> Tuberías, tanques de almacenamiento y ventilación para un suministro seguro y adecuado de gases y líquidos para cualquier propósito, incluyendo vacío, aire comprimido y gases para usos médicos, dentales, comerciales e industriales.</div><br>

     <div class="dot"><b>(c)</b> Todos los aparatos de gas, conductos de humo y conexiones de gas para todos los sistemas, incluidas las unidades de calefacción de espacios suspendidos. Esto no incluye unidades de aire caliente forzado.</div><br>

     <div class="dot"><b>(d)</b> Tuberías de agua y gas desde el lado del dueño de la propiedad del contador de servicios públicos hasta la estructura u obras fijas.</div><br>

     <div class="dot"><b>(e)</b> La instalación de cualquier tipo de equipo para calentar agua o fluidos a una temperatura adecuada para los fines enumerados en esta sección, incluida la instalación de equipos solares para este fin.</div><br>

     <div class="dot"><b>(f)</b> El mantenimiento y reemplazo de todos los elementos descritos anteriormente y todos los dispositivos de salud y seguridad, como las válvulas de terremoto de gas, las válvulas de control de gas, los dispositivos de prevención de reflujo, el equipo de acondicionamiento de agua y las válvulas de regulación.</div><br>
