     <div class="info"><p>   
  Las aseguradoras a menudo combinan una cantidad de coberturas de seguros en un paquete que se vende como un único contrato. La política más común para las pequeñas empresas es la Business Owner’s Policy (BOP).
<br><br>
El BOP combina la cobertura de todos los principales riesgos de seguro de propiedad y responsabilidad civil, así como muchas coberturas adicionales en una política de paquete adecuada para la mayoría de las pequeñas empresas. El término "BOP" se refiere específicamente al lenguaje de la póliza de seguro desarrollado (y revisado según sea necesario) por expertos en ISO. ISO proporciona un lenguaje de muestra de póliza de seguro, investigación y una variedad de otros productos a compañías de seguros.
<br><br>
El BOP incluye un seguro de ingresos comerciales, a veces llamado seguro de interrupción del negocio. Esto compensa al propietario de una empresa por la pérdida de ingresos después de un desastre. Los desastres normalmente interrumpen las operaciones y pueden forzar a una empresa a abandonar sus instalaciones. El seguro de ingresos comerciales también cubre el gasto adicional en que puede incurrir si una empresa debe operar fuera de una ubicación temporal.
<br><br>
Para cubrir los riesgos específicos asociados con un negocio, se pueden agregar una variedad de coberturas adicionales al BOP básico. Por ejemplo, si una empresa tiene un cartel al aire libre, el BOP no lo cubre a menos que la cobertura se agregue específicamente por una prima adicional. Si una empresa depende del comercio electrónico, el propietario puede agregar cobertura para los ingresos perdidos y los gastos adicionales en caso de que la capacidad de la empresa para llevar a cabo el comercio electrónico se frene o se detenga debido a un virus informático o un hacker.
<br><br>
Solo las pequeñas y medianas empresas que cumplen ciertos criterios son elegibles para un BOP. Los factores que las aseguradoras consideran incluyen el tamaño de las instalaciones, los límites de responsabilidad requeridos, el tipo de negocio y el alcance de la actividad fuera del sitio. Las primas para las políticas de BOP se basan en los factores más la ubicación del negocio, la estabilidad financiera, la construcción del edificio, las características de seguridad y los riesgos de incendio.

