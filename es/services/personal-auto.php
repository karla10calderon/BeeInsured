<div class="info"><p>
    Una póliza de seguro <b>Personal Auto</b> cubre todas las cosas que se consideran de naturaleza "personal". Estos incluyen sus unidades de ida y vuelta a su oficina para el trabajo, viajes a la tienda de comestibles y actividades de ocio. Su póliza de automóvil cubre los vehículos y los conductores domésticos que usted menciona específicamente en la póliza y está orientada a proteger a las personas que usan su automóvil para fines personales regulares.
<br><br>
Las políticas de <b>Personal Auto</b> solo cubren tipos específicos de uso de vehículos, registrados a nombre de una persona. Necesitará cobertura comercial si compra un automóvil a nombre de su compañía o posee un camión, autobús o vehículo que se considera de uso comercial y peso bruto del vehículo. A menudo hay un poco de área gris y confusión con respecto al uso de un auto personal para trabajos como entrega de periódicos o entrega de pizzas. Deberá consultar con su profesional de seguros para determinar si su compañía de seguros considera que ese tipo de trabajo es un riesgo aceptable.
<br><br>
La política de <b>Personal Auto</b> se puede escribir en vehículos elegibles que son propiedad y están alquilados por un individuo o por un esposo o esposa que residen en el mismo hogar. Un vehículo alquilado a otros para uso público no es elegible y debe estar asegurado bajo una política comercial.
<br><br>
La mayoría de los vehículos, como automóviles, minivans y vehículos utilitarios deportivos, son elegibles para una póliza de automóvil personal. Si se usan furgonetas, camiones u otros tipos de vehículos grandes para el transporte o la entrega de bienes y materiales, por lo general quedan descalificados para una póliza de automóvil personal.
  
